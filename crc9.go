package p25

// CRC-9 error detection for P25 Confirmed Packet Data blocks (TIA-102.BAAA).
//
// A confirmed data block is 144 bits after 3/4-rate trellis decoding, laid out
// as: DBSN (data block serial number) at bits [0:7], CRC-9 at bits [7:16], and
// 16 octets of payload at bits [16:144]. The CRC-9 protects the 135 non-CRC
// bits (DBSN + payload) which the checksum table treats as contiguous.
//
// The checksum table and the residual==0/0x1FF acceptance are ported verbatim
// from sdrtrunk's CRCP25 (CRC9_CHECKSUMS, generated with
// CRCUtil.generate(135, 9, 0x259, 0x1FF, false): polynomial 0x259, all-ones
// init). No bit-error correction is performed, matching sdrtrunk.
var crc9Checksums = [135]uint16{
	0x1E7, 0x1F3, 0x1F9, 0x1FC, 0x0D2, 0x045, 0x122, 0x0BD, 0x15E, 0x083,
	0x141, 0x1A0, 0x0FC, 0x052, 0x005, 0x102, 0x0AD, 0x156, 0x087, 0x143,
	0x1A1, 0x1D0, 0x0C4, 0x04E, 0x00B, 0x105, 0x182, 0x0ED, 0x176, 0x097,
	0x14B, 0x1A5, 0x1D2, 0x0C5, 0x162, 0x09D, 0x14E, 0x08B, 0x145, 0x1A2,
	0x0FD, 0x17E, 0x093, 0x149, 0x1A4, 0x0FE, 0x053, 0x129, 0x194, 0x0E6,
	0x05F, 0x12F, 0x197, 0x1CB, 0x1E5, 0x1F2, 0x0D5, 0x16A, 0x099, 0x14C,
	0x08A, 0x069, 0x134, 0x0B6, 0x077, 0x13B, 0x19D, 0x1CE, 0x0CB, 0x165,
	0x1B2, 0x0F5, 0x17A, 0x091, 0x148, 0x088, 0x068, 0x018, 0x020, 0x03C,
	0x032, 0x035, 0x11A, 0x0A1, 0x150, 0x084, 0x06E, 0x01B, 0x10D, 0x186,
	0x0EF, 0x177, 0x1BB, 0x1DD, 0x1EE, 0x0DB, 0x16D, 0x1B6, 0x0F7, 0x17B,
	0x1BD, 0x1DE, 0x0C3, 0x161, 0x1B0, 0x0F4, 0x056, 0x007, 0x103, 0x181,
	0x1C0, 0x0CC, 0x04A, 0x009, 0x104, 0x0AE, 0x07B, 0x13D, 0x19E, 0x0E3,
	0x171, 0x1B8, 0x0F0, 0x054, 0x006, 0x02F, 0x117, 0x18B, 0x1C5, 0x1E2,
	0x0DD, 0x16E, 0x09B, 0x14D, 0x1A6,
}

// checkCRC9 validates the CRC-9 of a 144-bit confirmed data block supplied as a
// bit-per-byte slice (MSB-first). The CRC field at bits [7:16] is excluded from
// the running checksum; payload bits [16:144] index the table at position-9.
// Returns true when the residual is 0 or all-ones (0x1FF), per the all-ones
// generator init.
func checkCRC9(bits144 []uint8) bool {
	if len(bits144) < 144 {
		return false
	}
	var calc uint16
	for i := 0; i < 144; i++ {
		if bits144[i]&1 == 0 {
			continue
		}
		switch {
		case i < 7:
			calc ^= crc9Checksums[i]
		case i > 15:
			calc ^= crc9Checksums[i-9]
		}
	}
	var checksum uint16
	for k := 7; k <= 15; k++ {
		checksum = (checksum << 1) | uint16(bits144[k]&1)
	}
	residual := calc ^ checksum
	return residual == 0 || residual == 0x1FF
}
